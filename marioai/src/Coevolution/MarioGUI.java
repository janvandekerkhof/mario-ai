    private void initRefLevels()
    {
        refLevels = new HashMap<>();
        preBMLevels = LevelGenerator.createLevelSegments(5, 0, 5, 0, 500);
        for (int i : difficulties) { // difficulties
            refLevels.put(i, new ArrayList());
            for (Level level : LevelGenerator.createLevelSegments(AMOUNT_REF_LEVELS,100, i, 0, 300)) {
                refLevels.get(i).add(level);
            }
        }
    }
    
    
