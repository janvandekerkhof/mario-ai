    private double[] benchmark(int agent){
        double[] benchmark = new double[difficulties.length];
        for (int i = 0; i < benchmark.length; i++) {
            benchmark[i] = 0;
        }
        for (int i = 0 ; i < difficulties.length; i++) {
            for (Level level : refLevels.get(difficulties[i])) {
                benchmark[i] += EM.runOne(agent, level, difficulties[i]);
            }
            benchmark[i] = benchmark[i] / (double)refLevels.get(difficulties[i]).size();
        }
        for (int i = 0; i < difficulties.length; i++) {
            if(!aBenchmarks.containsKey(difficulties[i]))
            {
                aBenchmarks.put(difficulties[i], new ArrayList());
            }
            aBenchmarks.get(difficulties[i]).add(benchmark[i]);
        }
        
        return benchmark;
    }
    
